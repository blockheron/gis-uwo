    private int handleSearch() {
        String query = searchBox.getText();
        if (query.isEmpty() || query.equals("Search")) {
            System.out.println("Empty query");
            return 1;
        }
        String filter = selectBox.getSelectedItem().toString();
        System.out.println(query + " " + filter);
        return 0;
    }

